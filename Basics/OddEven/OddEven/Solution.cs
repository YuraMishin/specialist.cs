namespace OddEven
{
  /// <summary>
  /// Class Solution.
  /// Implements odd or even calculation  /// 
  /// </summary>
  public class Solution
  {
    /// <summary>
    /// Method calculates the result
    /// </summary>
    /// <param name="number">Number</param>
    /// <returns>boolean</returns>
    public bool IsOdd(int number)
    {
      return number % 2 != 0;
    }
  }
}
